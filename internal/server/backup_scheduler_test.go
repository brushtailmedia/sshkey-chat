// newSchedulerTestServer builds a Server with a writable dataDir +
// configDir, ready to invoke tryScheduledBackup against. Differs
// from newTestServer in that it sets backupStartupGracePeriod=0
// (test cleanup restores the previous value) so the scheduler
// doesn't pause for 60s when started directly.
func newSchedulerTestServer(t *testing.T) *Server {
	t.Helper()
	prev := SetBackupStartupGracePeriod(0)
	t.Cleanup(func() { SetBackupStartupGracePeriod(prev) })

	configDir := t.TempDir()
	dataDir := t.TempDir()

	if err := os.WriteFile(filepath.Join(configDir, "server.toml"), []byte(`
[server]
port = 2222
bind = "127.0.0.1"

[server.auto_revoke]
enabled = false

[backup]
enabled = true
interval = "24h"
dest_dir = "backups"
retention_count = 5
retention_age = "168h"
compress = true
include_config_files = true
`), 0644); err != nil {
		t.Fatalf("write server.toml: %v", err)
	}
	if err := os.WriteFile(filepath.Join(configDir, "rooms.toml"), []byte{}, 0644); err != nil {
		t.Fatalf("write rooms.toml: %v", err)
	}
	// host_key is generated by Server.New (loadOrGenerateHostKey)
	// when missing; we don't pre-write a fake one because the server
	// would fail to parse it.

	cfg, err := config.Load(configDir)
	if err != nil {
		t.Fatalf("load config: %v", err)
	}
	logger := slog.New(slog.NewTextHandler(io.Discard, nil))
	s, err := New(cfg, logger, dataDir)
	if err != nil {
		t.Fatalf("New: %v", err)
	}
	t.Cleanup(func() {
		if s.store != nil {
			s.store.Close()
		}
		// Don't run Close() — tests don't start ListenAndServe so
		// the listener fields are nil and Close paths assume them.
		// Just remove the lockfile so the next New on same dir works.
		_ = os.Remove(filepath.Join(dataDir, lockfileName))
	})
	return s
}

// -------- tryScheduledBackup --------

func TestTryScheduledBackup_HappyPath(t *testing.T) {
	s := newSchedulerTestServer(t)

	s.tryScheduledBackup()

	// Stats sidecar should show 1 success.
	if got := s.BackupSuccessesTotal(); got != 1 {
		t.Errorf("successes_total = %d, want 1", got)
	}
	if got := s.BackupFailuresTotal(); got != 0 {
		t.Errorf("failures_total = %d, want 0", got)
	}

	// A tarball should exist under dataDir/backups.
	destDir := filepath.Join(s.dataDir, "backups")
	entries, err := os.ReadDir(destDir)
	if err != nil {
		t.Fatalf("ReadDir %s: %v", destDir, err)
	}
	tarballs := 0
	for _, e := range entries {
		if isBackupTarball(e.Name()) {
			tarballs++
		}
	}
	if tarballs != 1 {
		t.Errorf("expected 1 tarball, got %d (entries: %v)", tarballs, entries)
	}
}

func TestTryScheduledBackup_DisabledIsNoop(t *testing.T) {
	s := newSchedulerTestServer(t)
	// Flip enabled off in the loaded config.
	s.cfg.Server.Backup.Enabled = false

	s.tryScheduledBackup()

	if got := s.BackupSuccessesTotal(); got != 0 {
		t.Errorf("disabled scheduler should not record success, got %d", got)
	}
	if got := s.BackupFailuresTotal(); got != 0 {
		t.Errorf("disabled scheduler should not record failure, got %d", got)
	}
	// No tarball written.
	if _, err := os.Stat(filepath.Join(s.dataDir, "backups")); err == nil {
		entries, _ := os.ReadDir(filepath.Join(s.dataDir, "backups"))
		for _, e := range entries {
			if isBackupTarball(e.Name()) {
				t.Errorf("tarball written despite enabled=false: %s", e.Name())
			}
		}
	}
}

func TestTryScheduledBackup_MutexContention(t *testing.T) {
	s := newSchedulerTestServer(t)

	// Hold the mutex from one goroutine; second tryScheduledBackup
	// must return immediately without producing a tarball.
	s.backupMu.Lock()
	defer s.backupMu.Unlock()

	done := make(chan struct{})
	go func() {
		s.tryScheduledBackup()
		close(done)
	}()
	select {
	case <-done:
		// Fast — TryLock skipped as expected.
	case <-time.After(2 * time.Second):
		t.Fatal("tryScheduledBackup blocked on mutex; expected fast skip via TryLock")
	}

	if got := s.BackupSuccessesTotal(); got != 0 {
		t.Errorf("contended call should skip without recording success, got %d", got)
	}
}

func TestTryScheduledBackup_FailureIncrementsCounter(t *testing.T) {
	s := newSchedulerTestServer(t)
	// Force a failure: remove the host_key so backup.Run aborts on
	// the required-file check (IncludeConfigFiles=true by default).
	if err := os.Remove(filepath.Join(s.cfg.Dir, "host_key")); err != nil {
		t.Fatalf("remove host_key: %v", err)
	}

	s.tryScheduledBackup()

	if got := s.BackupFailuresTotal(); got != 1 {
		t.Errorf("failures_total = %d, want 1", got)
	}
	if got := s.BackupSuccessesTotal(); got != 0 {
		t.Errorf("successes_total = %d, want 0", got)
	}

	// Verify last_error_msg captured.
	stats := s.readBackupStats()
	if stats.LastErrorMsg == "" {
		t.Error("LastErrorMsg empty after failure")
	}
	if !strings.Contains(stats.LastErrorMsg, "host_key") {
		t.Errorf("LastErrorMsg = %q, expected to mention host_key", stats.LastErrorMsg)
	}
}

