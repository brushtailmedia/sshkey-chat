func TestTryScheduledBackup_SkipIfIdle_FilesChangeRunsAgain(t *testing.T) {
	// Write activity in data/files/ should defeat skip_if_idle on
	// the next tick. This is the practical "user uploaded an
	// attachment between scheduled backups" case.
	s := newSchedulerTestServer(t)

	s.tryScheduledBackup()
	if got := s.BackupSuccessesTotal(); got != 1 {
		t.Fatalf("first tick: %d, want 1", got)
	}

	// Drop a fake attachment blob into data/files/ so the dir
	// mtime increments. Sleep briefly to ensure mtime resolution
	// catches the change (some filesystems have second-granularity
	// mtimes).
	filesDir := filepath.Join(s.dataDir, "data", "files")
	if err := os.MkdirAll(filesDir, 0755); err != nil {
		t.Fatalf("mkdir files: %v", err)
	}
	time.Sleep(20 * time.Millisecond)
	if err := os.WriteFile(filepath.Join(filesDir, "file_test_blob"), []byte("new attachment"), 0644); err != nil {
		t.Fatalf("write blob: %v", err)
	}
	time.Sleep(20 * time.Millisecond)

	s.tryScheduledBackup()
	if got := s.BackupSuccessesTotal(); got != 2 {
		t.Errorf("second tick with new attachment: successes = %d, want 2", got)
	}
}

// -------- applyRetention --------

// seedTarballsByAge writes N fake tarballs in dir with mtimes spaced
// at oneAgeStep apart, oldest-to-newest. Returns names in
// chronological order (oldest first). Used to test retention's
// sort-by-mtime + cap behavior.
func seedTarballsByAge(t *testing.T, dir string, count int, oneAgeStep time.Duration) []string {
	t.Helper()
	if err := os.MkdirAll(dir, 0755); err != nil {
		t.Fatalf("mkdir: %v", err)
	}
	now := time.Now()
	var names []string
	for i := 0; i < count; i++ {
		// Spaced timestamps so sort by mtime is stable.
		ts := now.Add(-time.Duration(count-1-i) * oneAgeStep)
		name := fmt.Sprintf("backup-%s.tar.gz", ts.UTC().Format("20060102-150405"))
		path := filepath.Join(dir, name)
		if err := os.WriteFile(path, []byte("fake"), 0644); err != nil {
			t.Fatalf("write %s: %v", path, err)
		}
		if err := os.Chtimes(path, ts, ts); err != nil {
			t.Fatalf("chtimes %s: %v", path, err)
		}
		names = append(names, name)
	}
	return names
}

func TestApplyRetention_CountCap(t *testing.T) {
	dir := t.TempDir()
	// 8 tarballs spaced 1 hour apart. Retention: keep 3, no age cap.
	names := seedTarballsByAge(t, dir, 8, time.Hour)
	cfg := config.ParsedBackupConfig{RetentionCount: 3}

	if err := applyRetention(dir, cfg); err != nil {
		t.Fatalf("applyRetention: %v", err)
	}

	entries, _ := os.ReadDir(dir)
	if len(entries) != 3 {
		var got []string
		for _, e := range entries {
			got = append(got, e.Name())
		}
		t.Fatalf("expected 3 tarballs after retention, got %d: %v", len(entries), got)
	}
	// The 3 newest names should remain. names[0] is oldest, names[len-1] is newest.
	wantKept := map[string]bool{
		names[5]: true, names[6]: true, names[7]: true,
	}
	for _, e := range entries {
		if !wantKept[e.Name()] {
			t.Errorf("unexpected file kept: %s (expected newest 3)", e.Name())
		}
	}
}

func TestApplyRetention_AgeCap(t *testing.T) {
	dir := t.TempDir()
	// 5 tarballs spaced 2 hours apart. Retention: age cap 5h, no count cap.
	// → tarballs older than 5h are deleted.
	// Spacing 2h: ages are 8h, 6h, 4h, 2h, 0h. Older than 5h = 8h and 6h.
	seedTarballsByAge(t, dir, 5, 2*time.Hour)
	cfg := config.ParsedBackupConfig{RetentionAge: 5 * time.Hour}

	if err := applyRetention(dir, cfg); err != nil {
		t.Fatalf("applyRetention: %v", err)
	}

	entries, _ := os.ReadDir(dir)
	// Should have 3 left (4h, 2h, 0h).
	if len(entries) != 3 {
		var got []string
		for _, e := range entries {
			got = append(got, e.Name())
		}
		t.Errorf("expected 3 tarballs after age cap, got %d: %v", len(entries), got)
	}
}

func TestApplyRetention_BothCapsORSemantics(t *testing.T) {
	dir := t.TempDir()
	// 7 tarballs spaced 1h apart. count=10 (effectively no cap —
	// nothing exceeds the count), age=3h30m. Tarballs older than
	// 3h30m should go regardless of count. Half-hour offset keeps
	// the boundary unambiguous (no test-microsecond-drift risk).
	seedTarballsByAge(t, dir, 7, time.Hour)
	cfg := config.ParsedBackupConfig{
		RetentionCount: 10,                    // larger than total — count cap inactive
		RetentionAge:   3*time.Hour + 30*time.Minute, // age cap with clear boundary
	}

	if err := applyRetention(dir, cfg); err != nil {
		t.Fatalf("applyRetention: %v", err)
	}
	entries, _ := os.ReadDir(dir)
	// Ages from oldest are 6h,5h,4h,3h,2h,1h,0h. > 3h30m: 6h,5h,4h
	// (3 deletions). Keep 4.
	if len(entries) != 4 {
		var got []string
		for _, e := range entries {
			got = append(got, e.Name())
		}
		t.Errorf("with count=10 + age=3h30m on 7 tarballs spaced 1h: expected 4 kept, got %d: %v", len(entries), got)
	}
}

func TestApplyRetention_IgnoresNonTarballs(t *testing.T) {
	dir := t.TempDir()
	seedTarballsByAge(t, dir, 5, time.Hour)
	// Add a stats file and a random text file — neither should be touched.
	if err := os.WriteFile(filepath.Join(dir, ".backup-stats.json"), []byte("{}"), 0644); err != nil {
		t.Fatalf("seed stats: %v", err)
	}
	if err := os.WriteFile(filepath.Join(dir, "operator-notes.txt"), []byte("hello"), 0644); err != nil {
		t.Fatalf("seed notes: %v", err)
	}

	cfg := config.ParsedBackupConfig{RetentionCount: 1}
	if err := applyRetention(dir, cfg); err != nil {
		t.Fatalf("applyRetention: %v", err)
	}

	mustExist := []string{".backup-stats.json", "operator-notes.txt"}
	for _, name := range mustExist {
		if _, err := os.Stat(filepath.Join(dir, name)); err != nil {
			t.Errorf("retention deleted non-tarball %s: %v", name, err)
		}
	}
	// Tarball count should be 1.
	entries, _ := os.ReadDir(dir)
	tarballs := 0
	for _, e := range entries {
		if isBackupTarball(e.Name()) {
			tarballs++
		}
	}
	if tarballs != 1 {
		t.Errorf("expected 1 tarball after retention_count=1, got %d", tarballs)
	}
}

