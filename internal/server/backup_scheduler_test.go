// -------- resolveBackupDestDir --------

func TestResolveBackupDestDir(t *testing.T) {
	cases := []struct {
		name        string
		configDest  string
		dataDir     string
		want        string
	}{
		{"empty falls to default", "", "/data", "/data/backups"},
		{"relative anchored to dataDir", "snapshots", "/data", "/data/snapshots"},
		{"absolute used as-is", "/mnt/archive", "/data", "/mnt/archive"},
		{"deeply relative", "a/b/c", "/data", "/data/a/b/c"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got := resolveBackupDestDir(tc.configDest, tc.dataDir)
			if got != tc.want {
				t.Errorf("resolveBackupDestDir(%q, %q) = %q, want %q",
					tc.configDest, tc.dataDir, got, tc.want)
			}
		})
	}
}

