// -------- runBackupScheduler — integration-style smoke test --------

// TestRunBackupScheduler_StopsCleanlyOnDisabledConfig verifies that
// enabled=false causes the goroutine to exit immediately rather than
// running a no-op tick loop forever.
func TestRunBackupScheduler_StopsCleanlyOnDisabledConfig(t *testing.T) {
	s := newSchedulerTestServer(t)
	s.cfg.Server.Backup.Enabled = false

	done := make(chan struct{})
	go func() {
		s.runBackupScheduler()
		close(done)
	}()

	select {
	case <-done:
		// Good — exited fast.
	case <-time.After(2 * time.Second):
		// Need to stop it manually.
		close(s.backupSchedulerStop)
		t.Error("scheduler did not exit on enabled=false within 2s")
	}
}

// TestRunBackupScheduler_StopsOnSignal exercises the goroutine
// lifecycle: start with grace=0, immediate first backup runs,
// stop channel closes, scheduler exits.
func TestRunBackupScheduler_StopsOnSignal(t *testing.T) {
	s := newSchedulerTestServer(t)

	done := make(chan struct{})
	var wg sync.WaitGroup
	wg.Add(1)
	go func() {
		defer wg.Done()
		s.runBackupScheduler()
		close(done)
	}()

	// Wait for the immediate first backup to complete (a success
	// recorded means tryScheduledBackup ran).
	deadline := time.Now().Add(3 * time.Second)
	for time.Now().Before(deadline) {
		if s.BackupSuccessesTotal() >= 1 {
			break
		}
		time.Sleep(50 * time.Millisecond)
	}
	if got := s.BackupSuccessesTotal(); got < 1 {
		close(s.backupSchedulerStop)
		wg.Wait()
		t.Fatalf("first backup did not complete within 3s; successes = %d", got)
	}

	// Now signal stop.
	close(s.backupSchedulerStop)
	select {
	case <-done:
	case <-time.After(2 * time.Second):
		t.Error("scheduler did not exit on stop signal within 2s")
	}
}

